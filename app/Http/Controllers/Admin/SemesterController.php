     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $semester = Semester::create([
            'num_semester' => $request->input('num_semester'),
            'annee_univ' => $request->input('annee_univ')
        ]);
        if ($semester) {
            return redirect()->route('semester.create')->with('success', 'Successfully added !!!');
        }
        return redirect()->route('semester.create')->with('errors', 'error while creating new semester !!');
    }

